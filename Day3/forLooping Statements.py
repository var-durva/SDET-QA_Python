#Control statements
#2) Looping statements
#-----------------------------------------------------------------
#for loop
#Example 1:
for i in range(10):
    print(i)

#Example 2:
for i in range(1,10):
    print(i)

#Example 3:
for i in range(1,10,2):
    print(i)

#Example 4: even function
for i in range(0,21,2):
    print(i)

#Example 5:
for i in range(0,21,3):
    print(i)

#Example 6: 10 to 1 in decreasing order
for i in range(10,0,-1):
    print(i)

#Example 6: 5,10,15,20,,,,,100
for i in range(5,101,5):
    print(i)
